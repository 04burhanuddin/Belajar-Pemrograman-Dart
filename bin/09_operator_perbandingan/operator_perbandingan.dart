/*
? perbandingan ini digunakan untuk membandingkan dua buah nilai dan biasanya hasilnya adalah boolean dengan hasil (true dan false, jika benar dan salah)
! operator ini hanya bisa diunakan di tipe number dan string
*/

void main(List<String> args) {
  /*
  == sam dengan
  != tidak sama dengan
  > lebih dari
  < kurang dari
  >= lebih dari atau sama dengan
  <= kurang dari atau sama dengan
  */

  print(4 == 4);
  print(4 != 4);
  print(4 > 4);
  print(4 < 4);
  print(4 <= 4);
  print(4 >= 4);

  // contoh perbandingan string
  print('Burhan' == 'Burhan');
}
