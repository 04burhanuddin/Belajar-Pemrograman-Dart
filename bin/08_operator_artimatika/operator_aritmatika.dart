/*
+ pertambahan
-, -expression, pengurangan atau negatif
* perkalian
/ pembagian, hasil double
~/ pembagian, hasil int
% sisa bagi
*/
void main(List<String> args) {
  var result1 = 4 + 4;
  var result2 = 25 - 4;
  var result3 = -4;
  var result4 = 4;
  var result5 = 4 * 4;
  var result6 = 40 / 4;
  var result7 = 50 ~/ 4;
  var result8 = 4 % 4;

  print(result1);
  print(result2);
  print(result3);
  print(result4);
  print(result5);
  print(result6);
  print(result7);
  print(result8);

  // menggunakan variabel
  var angka1 = 30;
  var angka2 = 47;

  print(angka1 + angka2);
}
