/*
untuk menggunakan operator penugasan hanya menambahkan sama dengan setelah operator artimatika lihat contoh berikut
*/

void main(List<String> args) {
  var nilai = 40;

  // jika menggunakan operator artimatika
  nilai = nilai + 20;

  // kemudian di persingkat menggunakan operator penugasan
  nilai += 20;

  print(nilai);
}
